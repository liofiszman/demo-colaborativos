package Classes;

public class HorarioAtencion {
    private int id;
    private String diAtencion;

    public HorarioAtencion() {}
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getDiaAtencion() {
        return diAtencion;
    }
    public void setDiaAtencion(String diAtencion) {
        this.diAtencion = diAtencion;
    }
}
